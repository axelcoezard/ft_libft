#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	nbr;

	while (*nptr == ' ' || *nptr == '\t' || *nptr == '\v'
		|| *nptr == '\n' || *nptr == '\f' || *nptr == '\r')
		nptr++;
	sign = 1;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	nbr = 0;
	while (*nptr >= '0' && *nptr <= '9')
	{
		nbr *= 10;
		nbr += *nptr - '0';
		nptr++;
	}
	return (sign * nbr);
}
